package part1.section09_constructor;
/*
 * 생성자(Constructor)
 * 	객체를 생성할 때 호출되어 객체 초기화 담당
 * 	모든 클래스는 반드시 생성자를 가짐
 * 
 * 생성자 특징
 * 	생성자의 이름은 클래스 이름과 동일
 * 	반환 타입 없음
 * 	객체를 생성할 때 new 연산자와 함께 사용됨
 * 	모든 클래스는 최소 하나 이상의 생성자가 존재함 
 * 
 */
public class Constructor01 {
	
	public static void main(String[] args) {
		Snack snack = new Snack();
	}

}











